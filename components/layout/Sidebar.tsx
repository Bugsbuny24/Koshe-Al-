export function Sidebar() {
  const pathname = usePathname();
  const router = useRouter();
  const { sidebarOpen, setSidebarOpen, profile, quota } = useStore();

  const handleLogout = async () => {
    const supabase = createSupabaseClient();
    await supabase.auth.signOut();
    router.push('/login');
  };

  const planColors: Record<string, string> = {
    starter: 'text-slate-400',
    pro: 'text-accent-blue',
    ultra: 'text-pi-gold',
    growth: 'text-accent-green',
    studio: 'text-purple-400',
  };

  const planMaxCredits: Record<string, number> = {
    ultra: 5000,
    pro: 1000,
    growth: 300,
    studio: 2000,
  };

  const safePlanId = quota?.plan_id ?? 'starter';
  const safeCredits = quota && Number.isFinite(quota.credits_remaining) ? quota.credits_remaining : 0;
  const planMax = planMaxCredits[safePlanId] ?? 100;

  return (
    <>
      {/* Mobile overlay */}
      <AnimatePresence>
        {sidebarOpen && (
          <motion.div
            initial={{ opacity: 0 }}
            animate={{ opacity: 1 }}
            exit={{ opacity: 0 }}
            className="fixed inset-0 bg-black/60 z-30 lg:hidden"
            onClick={() => setSidebarOpen(false)}
          />
        )}
      </AnimatePresence>

      {/* Sidebar */}
      <motion.aside
        initial={false}
        animate={{ x: sidebarOpen ? 0 : -256 }}
        transition={{ duration: 0.25, ease: 'easeInOut' }}
        className="fixed left-0 top-0 bottom-0 w-64 bg-bg-deep border-r border-white/5 flex flex-col z-40 lg:translate-x-0"
      >
        {/* Logo */}
        <div className="h-16 flex items-center px-6 border-b border-white/5">
          <div className="flex items-center gap-2">
            <div className="w-8 h-8 rounded-lg bg-accent-blue flex items-center justify-center text-white font-bold text-sm shadow-lg shadow-accent-blue/30">
              K
            </div>
            <span className="font-display font-bold text-xl text-white tracking-tight">Koschei</span>
          </div>
        </div>

        {/* Nav */}
        <nav className="flex-1 px-4 py-6 space-y-1 overflow-y-auto">
          {navItems.map((item) => {
            const isActive = pathname === item.href || pathname.startsWith(item.href + '/');
            return (
              <Link
                key={item.href}
                href={item.href}
                className={cn(
                  'flex items-center gap-3 px-3 py-2.5 rounded-xl text-sm font-medium transition-all duration-150',
                  isActive
                    ? 'bg-accent-blue/15 text-accent-blue border border-accent-blue/20'
                    : 'text-slate-400 hover:text-white hover:bg-white/5'
                )}
              >
                {item.icon}
                {item.label}
                {isActive && (
                  <motion.div
                    layoutId="activeNav"
                    className="ml-auto w-1.5 h-1.5 rounded-full bg-accent-blue"
                  />
                )}
              </Link>
            );
          })}
        </nav>

        {/* Credits + Plan */}
        <div className="p-4 border-t border-white/5 space-y-3">
          {quota && (
            <div className="bg-bg-card rounded-xl p-3">
              <div className="flex items-center justify-between mb-2">
                <span className="text-xs text-slate-500">Krediler</span>
                <span className={cn('text-xs font-semibold', planColors[safePlanId] || 'text-slate-400')}>
                  {safePlanId.toUpperCase()}
                </span>
              </div>
              <div className="flex items-baseline gap-1">
                <span className="text-lg font-bold text-white">{Math.floor(safeCredits)}</span>
                <span className="text-xs text-slate-500">kredi kaldı</span>
              </div>
              <div className="mt-2 h-1.5 bg-bg-deep rounded-full overflow-hidden">
                <div
                  className="h-full bg-gradient-to-r from-accent-blue to-accent-green rounded-full transition-all"
                  style={{ width: `${Math.min(100, (safeCredits / planMax) * 100)}%` }}
                />
              </div>
            </div>
          )}

          {/* User + Logout */}
          <div className="flex items-center gap-3">
            {profile?.avatar_url && (
              <Image src={profile.avatar_url} alt="" width={32} height={32} className="rounded-lg" unoptimized />
            )}
            <div className="flex-1 min-w-0">
              <p className="text-xs font-medium text-white truncate">{profile?.full_name || 'Kullanıcı'}</p>
              <p className="text-xs text-slate-500 truncate">{profile?.email}</p>
            </div>
            <button
              onClick={handleLogout}
              className="p-1.5 text-slate-400 hover:text-red-400 transition-colors"
              title="Çıkış Yap"
            >
              <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
              </svg>
            </button>
          </div>
        </div>
      </motion.aside>
    </>
  );
}
